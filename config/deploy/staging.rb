# Simple Role Syntax
# ==================
# Supports bulk-adding hosts to roles, the primary server in each group
# is considered to be the first unless any hosts have the primary
# property set.  Don't declare `role :all`, it's a meta role.

set :stage, :staging
set :deploy_to, '~/apps/kjume'
set :branch, 'develop'
set :rails_env, 'production'
set :unicorn_config_path, "config/staging/unicorn.rb"

role :app, %w{deploy@staging.kjume.com}
role :web, %w{deploy@staging.kjume.com}
role :db,  %w{deploy@staging.kjume.com}

server 'staging.kjume.com', user: 'deploy', roles: %w{web app}, my_property: :my_value


